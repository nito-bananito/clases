#Funcion para calcular la resta de dos numeros
def resta(a,b):
    return a-b

#Funcion para dividir dos numeros
def divicion(a,b):
    return a/b

#Funcion para sumar dos numeros
def suma(a,b):
    return a+b

#Funcion para multiplicar dos numeros
def multiplicacion(a,b):
    return a*b